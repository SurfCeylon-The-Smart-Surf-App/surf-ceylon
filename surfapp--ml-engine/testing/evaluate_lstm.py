"""
LSTM Model - Accuracy Evaluation
=================================
Loads the ALREADY-TRAINED LSTM model, scalers, and the pre-built .npy
timeseries data, recreates the same 80/20 test split used during training
(random_state=42), runs predictions, and reports full accuracy metrics.

NO retraining required. Runtime: ~2–5 minutes (inference only).

Usage:
    cd surfapp--ml-engine
    .\\venv\\Scripts\\activate          # Required — tensorflow is inside venv
    python testing/evaluate_lstm.py
"""

import os
import sys
import numpy as np
import joblib
from sklearn.model_selection import train_test_split

# ─── Feature definitions (must match training script) ─────────────────────────
FEATURE_NAMES = [
    'Wave Height (m)',
    'Swell Height (m)',
    'Swell Period (s)',
    'Wind Speed (m/s)',
    'Wind Direction (°)',
    'Sea Level (m)',
]
FEATURE_UNITS = ['m', 'm', 's', 'm/s', '°', 'm']

# Industry-standard acceptable MAE thresholds per feature
BENCHMARK_MAE = [
    0.20,   # Wave Height   ±0.20 m
    0.15,   # Swell Height  ±0.15 m
    1.50,   # Swell Period  ±1.5 s
    1.50,   # Wind Speed    ±1.5 m/s
    20.0,   # Wind Dir      ±20° (circular variable, harder to predict)
    0.10,   # Sea Level     ±0.10 m
]


# ─── Helper ───────────────────────────────────────────────────────────────────

def mape(y_true, y_pred):
    """Mean Absolute Percentage Error (skips zeros to avoid division by zero)"""
    mask = y_true != 0
    if mask.sum() == 0:
        return float('nan')
    return float(np.mean(np.abs((y_true[mask] - y_pred[mask]) / y_true[mask])) * 100)


# ─── Main ─────────────────────────────────────────────────────────────────────

def main():
    print("=" * 70)
    print("  LSTM MODEL — ACCURACY EVALUATION")
    print("  No retraining. Evaluates on the same held-out 20% test split.")
    print("=" * 70)

    BASE_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    model_dir = os.path.join(BASE_DIR, 'models')
    artifact_dir = os.path.join(BASE_DIR, 'artifacts')

    # ── Step 1: Load TensorFlow / LSTM model ────────────────────────────────
    print("\n[1/6] Loading LSTM model...")
    model_path = os.path.join(
        model_dir, 'wave_forecast_multioutput_lstm.keras')
    try:
        import tensorflow as tf
        # force CPU — avoids GPU memory issues
        tf.config.set_visible_devices([], 'GPU')
        model = tf.keras.models.load_model(model_path)
        print(f"  ✅ Model loaded: {os.path.basename(model_path)}")
        print(f"     Parameters:  {model.count_params():,}")
        input_shape = model.input_shape
        output_shape = model.output_shape
        print(f"     Input shape:  {input_shape}")
        print(f"     Output shape: {output_shape}")
    except FileNotFoundError:
        print(f"  ❌ Model not found: {model_path}")
        print("     Run training first: python training/train_wave_forecast_lstm.py")
        sys.exit(1)
    except ImportError:
        print("  ❌ TensorFlow not installed. Activate the venv:")
        print("     .\\venv\\Scripts\\activate")
        sys.exit(1)

    # ── Step 2: Load scalers ─────────────────────────────────────────────────
    print("\n[2/6] Loading scalers...")
    scaler_x_path = os.path.join(
        model_dir, 'wave_forecast_scaler_X_multioutput.joblib')
    scaler_y_path = os.path.join(
        model_dir, 'wave_forecast_scaler_y_multioutput.joblib')
    try:
        scaler_X = joblib.load(scaler_x_path)
        scaler_y = joblib.load(scaler_y_path)
        print(f"  ✅ scaler_X loaded  (StandardScaler for input sequences)")
        print(f"  ✅ scaler_y loaded  (StandardScaler for output sequences)")
    except FileNotFoundError as e:
        print(f"  ❌ Scaler not found: {e}")
        sys.exit(1)

    # ── Step 3: Load .npy data ───────────────────────────────────────────────
    print("\n[3/6] Loading time-series data (.npy)...")
    X_path = os.path.join(artifact_dir, 'timeseries_X_multioutput.npy')
    y_path = os.path.join(artifact_dir, 'timeseries_y_multioutput.npy')
    try:
        X_raw = np.load(X_path)
        y_raw = np.load(y_path)
        n_samples, n_timesteps, n_features = X_raw.shape
        print(
            f"  ✅ X shape: {X_raw.shape}  (samples × {n_timesteps} hours × {n_features} features)")
        print(
            f"  ✅ y shape: {y_raw.shape}  (samples × {n_timesteps} hours × {n_features} features)")
    except FileNotFoundError:
        print(f"  ❌ .npy data not found at: {artifact_dir}")
        print("     These are generated by the training script. Re-run training once to build them.")
        sys.exit(1)

    # ── Step 4: Scale X and y (same as training) ─────────────────────────────
    print("\n[4/6] Scaling data (StandardScaler)...")
    X_flat = X_raw.reshape(-1, n_features)
    y_flat = y_raw.reshape(-1, n_features)
    X_scaled = scaler_X.transform(X_flat).reshape(
        n_samples, n_timesteps, n_features)
    y_scaled = scaler_y.transform(y_flat).reshape(
        n_samples, n_timesteps, n_features)
    print(f"  ✅ X and y scaled")

    # ── Step 5: Recreate the exact same 80/20 split as training ──────────────
    print("\n[5/6] Creating test split (random_state=42, same as training)...")
    _, X_test_sc, _, y_test_sc = train_test_split(
        X_scaled, y_scaled, test_size=0.2, random_state=42
    )
    n_test = X_test_sc.shape[0]
    print(f"  Total samples: {n_samples:,}")
    print(f"  Test samples:  {n_test:,}  (20%)")

    # ── Step 6: Predict ───────────────────────────────────────────────────────
    print(f"\n[6/6] Running inference on {n_test:,} test sequences...")
    print(
        f"  Each sequence covers {n_timesteps} hours — this may take a few minutes...")
    y_pred_sc = model.predict(X_test_sc, batch_size=32, verbose=1)

    # Inverse-transform both predictions and ground truth
    y_pred = scaler_y.inverse_transform(
        y_pred_sc.reshape(-1, n_features)
    ).reshape(n_test, n_timesteps, n_features)

    y_true = scaler_y.inverse_transform(
        y_test_sc.reshape(-1, n_features)
    ).reshape(n_test, n_timesteps, n_features)

    # Flatten to (samples × timesteps, features) for metric computation
    y_true_flat = y_true.reshape(-1, n_features)
    y_pred_flat = y_pred.reshape(-1, n_features)

    # ── Results ───────────────────────────────────────────────────────────────
    print(f"\n{'=' * 70}")
    print(
        f"  RESULTS — Held-out test set ({n_test:,} sequences × {n_timesteps} hours)")
    print(f"{'=' * 70}")

    print(f"\n  {'Feature':<22} {'MAE':>12}  {'RMSE':>12}  {'MAPE':>7}   Status")
    print("  " + "─" * 66)

    for i, (name, unit, bench) in enumerate(zip(FEATURE_NAMES, FEATURE_UNITS, BENCHMARK_MAE)):
        mae_val = float(np.mean(np.abs(y_true_flat[:, i] - y_pred_flat[:, i])))
        rmse_val = float(
            np.sqrt(np.mean((y_true_flat[:, i] - y_pred_flat[:, i]) ** 2)))
        mape_val = mape(y_true_flat[:, i], y_pred_flat[:, i])
        status = "✅ Good" if mae_val <= bench else "⚠️  High error"
        print(
            f"  {name:<22} {mae_val:>8.4f} {unit:<3}  {rmse_val:>8.4f} {unit:<3}  {mape_val:>5.1f}%   {status}")

    overall_mse = float(np.mean((y_true_flat - y_pred_flat) ** 2))
    overall_mae = float(np.mean(np.abs(y_true_flat - y_pred_flat)))
    print(f"\n  {'Overall MAE  (all features combined)':<40}  {overall_mae:.4f}")
    print(
        f"  {'Overall MSE  (training loss — lower is better)':<40}  {overall_mse:.6f}")

    # ── Accuracy by forecast horizon ─────────────────────────────────────────
    print(f"\n{'=' * 70}")
    print(f"  MAE BY FORECAST HORIZON  (wave height only — shows error growth)")
    print(f"{'=' * 70}")
    # hours: 1h,6h,12h,1d,2d,3d,4d,5d,6d,7d
    horizons = [0, 5, 11, 23, 47, 71, 95, 119, 143, 167]
    labels = ['H+1', 'H+6', 'H+12', 'Day 1', 'Day 2',
              'Day 3', 'Day 4', 'Day 5', 'Day 6', 'Day 7']
    print(f"\n  {'Horizon':<10} {'MAE (m)':>10}  Bar")
    for h, label in zip(horizons, labels):
        h_mae = float(np.mean(np.abs(y_true[:, h, 0] - y_pred[:, h, 0])))
        bar = '█' * int(h_mae * 40)
        print(f"  {label:<10} {h_mae:>8.4f}m  {bar}")

    # ── Sample prediction table ───────────────────────────────────────────────
    print(f"\n{'=' * 70}")
    print(f"  SAMPLE — First 3 hours of first test sequence")
    print(f"{'=' * 70}")
    print(
        f"\n  {'Hr':<5} {'Feature':<22} {'Actual':>10}  {'Predicted':>10}  {'|Error|':>10}")
    print("  " + "─" * 60)
    for h in range(3):
        for i, (name, unit) in enumerate(zip(FEATURE_NAMES, FEATURE_UNITS)):
            actual = y_true[0, h, i]
            predicted = y_pred[0, h, i]
            error = abs(actual - predicted)
            print(
                f"  {h+1:<5} {name:<22} {actual:>8.3f} {unit:<2}  {predicted:>8.3f} {unit:<2}  {error:>8.4f} {unit:<2}")
        if h < 2:
            print()

    # ── Metric explanation ────────────────────────────────────────────────────
    print(f"\n{'=' * 70}")
    print(f"  METRIC GUIDE")
    print(f"{'=' * 70}")
    print("""
  ┌─────────────────────────────────────────────────────────────────────┐
  │ MAE (Mean Absolute Error)                                           │
  │   Formula : mean( |y_true - y_pred| ) across ALL hours & sequences  │
  │   Meaning : Average prediction error in the original unit.          │
  │   Example : MAE=0.15m → wave heights are off by ±15 cm on average.  │
  │   Best for: human-readable accuracy summary in real units.          │
  ├─────────────────────────────────────────────────────────────────────┤
  │ RMSE (Root Mean Squared Error)                                      │
  │   Formula : sqrt( mean( (y_true - y_pred)² ) )                      │
  │   Meaning : Like MAE but occasional large errors are penalised more. │
  │   Note    : Always ≥ MAE. Large gap = the model sometimes makes      │
  │             significant errors even if the average looks acceptable. │
  ├─────────────────────────────────────────────────────────────────────┤
  │ MAPE (Mean Absolute Percentage Error)                               │
  │   Formula : mean( |y_true - y_pred| / |y_true| ) × 100%            │
  │   Meaning : Average error expressed as a percentage of the actual.  │
  │   Example : MAPE=12% → predictions are about 12% off on average.    │
  │   Caution : Unreliable when actual values are near zero (direction).  │
  ├─────────────────────────────────────────────────────────────────────┤
  │ MSE (Mean Squared Error) — Training Loss                            │
  │   Formula : mean( (y_true - y_pred)² )                              │
  │   Meaning : This is what the LSTM minimised during training.        │
  │   Note    : Units are squared — report MAE/RMSE for readability.    │
  ├─────────────────────────────────────────────────────────────────────┤
  │ Horizon MAE (Accuracy vs Forecast Distance)                         │
  │   Shows how MAE grows the further into the future we predict.       │
  │   Day 1 error < Day 7 error is expected — uncertainty accumulates.  │
  └─────────────────────────────────────────────────────────────────────┘

  Why LSTM metrics differ from Random Forest:
    • Random Forest predicts a SINGLE timestep from current conditions.
    • LSTM predicts 168 FUTURE hours from the past 168 hours.
    • LSTM errors compound — Day 7 is harder to predict than Hour 1.
    • A good LSTM Wave Height MAE is < 0.30 m (vs < 0.20 m for RF).
""")


if __name__ == '__main__':
    main()
